#' ozdata: Import and explore freely available Australian data sets
#'
#' There are many high quality data sets that are freely available for
#' Australia. Unfortunately they can be difficult to obtain and analyse.  Here
#' we provide tools to programmatically import and explore Australian data sets.
#' Data can be obtained from the official Australian government portal, which
#' contains over 40,000 data sets (<https://data.gov.au>).  Additionally,
#' economic data sets can be obtained from the Australian Macro Database
#' (<http://ozmacrodata.org>).  This project was created during the 2017 BURGr R
#' UnConference <https://github.com/AU-BURGr/UnConf2017>.
#'
#' @docType package
#'
#' @name ozdata
#'
#' @seealso \code{\link{get_ausmacrodata}} Read data from ausmacrodata.org
#'
#' @seealso \code{\link{download_oz_metadata}} Download meta-data from
#'  \url{https://data.gov.au}
#'
#' @seealso \code{\link{get_oz_dataset}} Get data set from Australian data
#'  portal
#'
#' @seealso \code{\link{get_url_dataset}} Get a data set from a URL retreived
#'  from the Australian datasets on \url{https://data.gov.au}
#'
#' @seealso \code{\link{search_metadata}} Search meta-data for the Australian
#' datasets on \url{https://data.gov.au}
#'
#' @seealso \code{\link{get_url}} Get the download URL for an Oz dataset using
#' its ID
#'
#' @seealso \code{\link{data}} Meta-data for all data on
#'  \url{http://data.gov.au}
#'
#' @author Jonathan Carroll, Maria Dmitrijeva, Jeffrey O. Hanson, Rob J. Hyndman
#' Simon Lyons, Cameron Roach, Adam H. Sparks and Matt Sutton
